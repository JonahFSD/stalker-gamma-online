describe("Sanity: vector class", function()
    it("vector() creates zero vector", function()
        local v = vector()
        assert_eq(0, v.x)
        assert_eq(0, v.y)
        assert_eq(0, v.z)
    end)

    it("vector():set(x,y,z) stores values", function()
        local v = vector():set(1.5, 2.5, 3.5)
        assert_eq(1.5, v.x)
        assert_eq(2.5, v.y)
        assert_eq(3.5, v.z)
    end)

    it("vector():set returns self", function()
        local v = vector()
        local ret = v:set(10, 20, 30)
        assert_true(ret == v)
        assert_eq(10, ret.x)
    end)
end)

describe("Sanity: alife() singleton", function()
    before_each(function()
        reset_engine()
    end)

    it("alife() returns non-nil singleton", function()
        assert_not_nil(alife())
    end)

    it("alife() has a metatable with __index as table", function()
        local mt = getmetatable(alife())
        assert_not_nil(mt)
        assert_eq("table", type(mt.__index))
    end)

    it("set_mp_client_mode / mp_client_mode round-trip", function()
        alife():set_mp_client_mode(true)
        assert_true(alife():mp_client_mode())
        alife():set_mp_client_mode(false)
        assert_false(alife():mp_client_mode())
    end)

    it("create() assigns incrementing IDs >= 1000", function()
        local pos = vector():set(0, 0, 0)
        local s1 = alife():create("stalker_bandit", pos, 0, 0)
        local s2 = alife():create("stalker_bandit", pos, 0, 0)
        assert_true(s1.id >= 1000)
        assert_true(s2.id > s1.id)
    end)

    it("create() fires server_entity_on_register synchronously", function()
        local fired = false
        RegisterScriptCallback("server_entity_on_register", function(se_obj)
            fired = true
            assert_not_nil(se_obj)
            assert_eq("bloodsucker_weak", se_obj:section_name())
        end)
        local pos = vector():set(10, 0, 10)
        alife():create("bloodsucker_weak", pos, 5, 5)
        assert_true(fired, "callback should have fired synchronously")
    end)

    it("object() returns the created entity", function()
        local pos = vector():set(5, 5, 5)
        local se = alife():create("stalker_bandit", pos, 1, 1)
        assert_not_nil(alife():object(se.id))
        assert_eq(se.id, alife():object(se.id).id)
    end)

    it("release() removes entity and fires server_entity_on_unregister", function()
        local pos = vector():set(0, 0, 0)
        local se = alife():create("stalker_bandit", pos, 0, 0)
        local id = se.id
        local fired = false
        RegisterScriptCallback("server_entity_on_unregister", function(obj)
            fired = true
        end)
        alife():release(se, true)
        assert_nil(alife():object(id))
        assert_true(fired)
    end)
end)

