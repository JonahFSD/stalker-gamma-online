describe("Sanity: gns mock", function()
    before_each(function()
        reset_gns()
    end)

    it("send_reliable captures payload", function()
        gns.send_reliable(1, "ES|id=42|section=stalker_bandit")
        local sent = gns._get_sent_reliable()
        assert_eq(1, #sent)
        assert_eq("ES|id=42|section=stalker_bandit", sent[1].payload)
    end)

    it("send_unreliable captures payload", function()
        gns.send_unreliable(-1, "EP|1,10.0,20.0,30.0,1.0")
        local sent = gns._get_sent_unreliable()
        assert_eq(1, #sent)
    end)

    it("inject_message / poll returns message", function()
        gns._inject_message(1, "WS|preset=storm", 14)
        local msgs = gns.poll()
        assert_eq(1, #msgs)
        assert_eq("WS|preset=storm", msgs[1].data)
    end)

    it("inject_event / poll_events returns event", function()
        gns._inject_event(1, "connected", 2)
        local evts = gns.poll_events()
        assert_eq(1, #evts)
        assert_eq(1, evts[1].event_type)
        assert_eq(2, evts[1].conn_id)
    end)
end)

